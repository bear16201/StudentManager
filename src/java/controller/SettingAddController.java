    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int type_id = Integer.parseInt(request.getParameter("type_id"));
        String setting_title = request.getParameter("setting_title");
        String subject_name = request.getParameter("subject_name");
        String display_order = request.getParameter("display_order");
        String setting_value = request.getParameter("setting_value");
        int status = Integer.parseInt(request.getParameter("status"));
        String description = request.getParameter("description");

        AdminDao dao = new AdminDao();
        dao.addSubject(type_id, setting_title, subject_name, setting_value, display_order, status, description);
        response.sendRedirect("settinglist");
    }

